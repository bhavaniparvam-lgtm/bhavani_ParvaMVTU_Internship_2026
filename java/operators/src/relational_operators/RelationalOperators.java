package relational_operators;

public class RelationalOperators {
	public static void main(String[] args) {
		int a=2;
		int b=6;
		//greater than
		System.out.println("a>b:"+(a>b));
		//less than
		System.out.println("a<b:"+(a<b));
		//less than or equal to
		System.out.println("a<=b:"+(a<=b));
		//greater than or equal to
		System.out.println("a>=b:"+(a>=b));
		//equal to
		System.out.println("a==b:"+(a==b));
		//not equal to
		System.out.println("a!=b:"+(a!=b));
		
	}
}
